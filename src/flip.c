#include <stdio.h>
#include <math.h>
#include "global.h"

void flip_blob(the_blobguts)
Blob_internal *the_blobguts;
{
   double newa2,newth;
   
   newa2 = 1.0/(*the_blobguts).a2;
   newth  = (*the_blobguts).th-M_PI_2;
   
   (*the_blobguts).a2 = newa2;
   (*the_blobguts).th = newth;
}



